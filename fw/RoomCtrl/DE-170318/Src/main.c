static void RAM_Init(void)
{
    uint8_t ee_buff[128];
    
    if(SPI_FLASH_ReleasePowerDown() == 0U) Error_Handler();

    ee_buff[0] = 0x00U;
    ee_buff[1] = 0x00U;
    if (HAL_I2C_Master_Transmit(&hi2c1, I2C_EE_WRITE, ee_buff, 2, I2C_EE_TIMEOUT) != HAL_OK) Error_Handler();
    if (HAL_I2C_Master_Receive(&hi2c1, I2C_EE_READ, ee_buff, 128, I2C_EE_TIMEOUT) != HAL_OK) Error_Handler();
    
    ROOM_Status                     = (ROOM_StatusTypeDef) ee_buff[EE_ROOM_STATUS_ADDRESS];  // room status address
    fw_update_status                = ee_buff[EE_FW_UPDATE_STATUS];              // firmware update status
    rs485_interface_baudrate        = ee_buff[EE_RS485_BAUDRATE];                // rs485 interface baudrate
    lcd_brightness                  = (ee_buff[EE_LCD_BRIGHTNESS] << 8U);        // lcd display backlight LED 
    lcd_brightness                  += ee_buff[EE_LCD_BRIGHTNESS + 1U];          // lcd display backlight LED 
  
    for(uint32_t t = 0U; t < 2U; t++)
    {
        rs485_interface_address[t]  = ee_buff[EE_RS485_INTERFACE_ADDRESS + t];   // rs485 device address
        rs485_group_address[t]      = ee_buff[EE_RS485_GROUP_ADDRESS + t];       // rs485 group broadcast address
        rs485_broadcast_address[t]  = ee_buff[EE_RS485_BROADCAST_ADDRESS + t];   // rs485 broadcast address msb
        system_id[t]                = ee_buff[EE_SYSTEM_ID_ADDRESS + t];         // system id (system unique number)
    }   
    
    for(uint32_t t = 0U; t < 5U; t++)
    {
        aRoomPowerExpiryDateTime[t] = ee_buff[EE_ROOM_POWER_TIMEOUT + t];    // room power expiry date time
    }

    for(uint32_t t = 0U; t < 6U; t++)
    {
        mifare_key_a[t] = ee_buff[EE_MIFARE_KEY_A + t];  // mifare access authentication key A 
        mifare_key_b[t] = ee_buff[EE_MIFARE_KEY_B + t];  // mifare access authentication key B 
    }
 
    for(uint32_t t = 0U; t < 8U; t++)
    {
        permitted_add[t][0] = ee_buff[EE_PERMITTED_ADDRESS_1 + (t * 2U)];        // additional permited address 1
        permitted_add[t][1] = ee_buff[EE_PERMITTED_ADDRESS_1 + 1U + (t * 2U)];   // additional permited address 1
    }    
    
    for(uint32_t t = 0U; t < 9U; t++)
    {
        thermostat_addresse[t]  = ee_buff[EE_ONEWIRE_ADDRESS_1 + t]; // onewire Lux thermostat address 1 or DS18B20 id 1
    }
    
    if (fw_update_status == BOOTLOADER_STATUS_UPDATE_SUCCESS)
    {
        SYS_UpdateSuccessSet();
        DISPLAY_FirmwareUpdated();
    }
    else if(fw_update_status == BOOTLOADER_STATUS_UPDATE_FAIL)
    {
        SYS_UpdateFailSet();
        DISPLAY_FirmwareUpdateFail();
    }

    if (fw_update_status != 0U)
    {
        fw_update_status = 0U;
        EEPROM_Save(EE_FW_UPDATE_STATUS, &fw_update_status, 1U);
    }
	
	if(!IS_09(rs485_interface_baudrate))
	{
		rs485_interface_baudrate = RS485_BAUDRATE_115200;
	}
}


